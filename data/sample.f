discours DU TRÔNE 
le Président : 
honorables sénateurs et sénatrices , 
mesdames et Messieurs les députés , 
mesdames et Messieurs , 
je ai le honneur aujourd' hui de ouvrir cette deuxième session de la trente - sixième législature de le Canada . 
je suis tout à fait consciente de les sacrifices que vous êtes si nombreux à faire sur les plans personnel et professionnel en acceptant les défis que représente une charge publique . 
je partage le engagement que vous avez pris et je me réjouis à la perspective de travailler avec vous durant les cinq prochaines années à le service de le peuple canadien . 
les représentants de le peuple canadien sont réunis aujourd' hui pour ouvrir la session de le Parlement qui mènera le pays vers le nouveau millénaire . 
À le seuil de un nouveau siècle , nous avons confiance dans le avenir que promet le Canada à nos enfants et à nos petits - enfants . 
la technologie modifie tous les aspects de notre vie . 
le savoir et la créativité constituent désormais le moteur de la nouvelle économie . 
et la collaboration devient plus essentielle à mesure que les défis présentés à notre société pluraliste gagnent en complexité . 
cependant , dans ce monde en mutation , les Canadiens et les Canadiennes connaîtront le succès , comme ils le ont toujours fait tout à le long de le histoire de notre pays . 
la promesse que incarne le Canada est née à une époque où les pays étaient formés à le creuset de les guerres ou de les révolutions . 
les fondateurs de notre nation ont choisi une voie qui est devenue la manière canadienne : ils ont créé un pays attaché à la paix , à le ordre et à le bon gouvernement pour tous ses citoyens . 
il aura fallu beaucoup de vision et de volonté pour briser le moule de le État - nation fondé sur une langue , une culture ou une religion unique . 
cette vision et cette volonté ont été largement récompensées . 
À le origine , le Canada , en tant que petite colonie peu industrialisée , ne jouait aucun rôle qui lui était propre dans les affaires mondiales . 
À le fil de les générations , les Canadiens et les Canadiennes sont parvenus à créer un avenir meilleur pour leurs familles et leurs collectivités . 
les Canadiens et leurs gouvernements ont surmonté les obstacles créés par la distance et les rigueurs de un climat nordique ; ils ont construit un chemin de fer national et un réseau de autoroutes , et créé un service postal , de les institutions culturelles nationales , de les hôpitaux , de les universités et de autres institutions . 
ils ont en outre créé un régime de sécurité sociale moderne . 
toutes ces réalisations constituent les assises de notre qualité de vie . 
en le espace de quelques générations , nous sommes devenus une nation indépendante , dotée de une économie industrielle avancée , qui a voix à le chapitre dans les instances internationales - les Nations unies , le G8 , le Organisation de coopération et de développement économiques , le OTAN , et nombre de autres . 
lorsque la tyrannie menaçait la paix et la sécurité dans le monde , les Canadiens ne ont jamais hésité à répondre à le appel . 
avec nos courageux anciens combattants , nous nous souvenons de ceux et celles qui ont payé de leur vie , que ce soit sur la crête de Vimy , à Juno Beach , ou sur la colline 355 , en Corée . 
héritiers de la tradition de les coureurs de les bois , nous avons exploré les frontières de la science . 
de le invention de le papier journal à la création de langages informatiques avancés , le ingéniosité de les Canadiens a contribué à édifier l' ère de le information . 
grâce notamment à la découverte de le insuline et à le premier stimulateur cardiaque , les Canadiens ont permis à de les millions de gens à travers le monde de vivre mieux plus longtemps . 
le Canada a été un pionnier de le utilisation pacifique de le espace ; il est devenu un leader dans le domaine de les communications par satellite et dans celui de la télédétection . 
aujourd' hui , nos astronautes utilisent la technologie canadienne pour aider à assembler la station spatiale internationale , le plus grand projet scientifique de le histoire . 
dans un monde toujours plus complexe , de les approches , de les compétences et de les idées diverses sont essentielles pour améliorer la qualité de vie . 
le Canada est un pays bilingue où de les hommes et de les femmes de un grand nombre de cultures , de races et de religions différentes participent à la vie économique , sociale et politique . 
ils puisent dans leur diversité la force et la créativité qui contribuent à façonner un pays moderne , tourné vers le avenir . 
nos actions et notre histoire font que nous nous sentons à le aise dans un monde marqué par le changement et une interdépendance toujours croissante . 
les talents de nos citoyens , les valeurs auxquelles nous sommes attachés et notre volonté de travailler ensemble permettront à le Canada de maintenir son rôle de premier plan dans le économie de le savoir . 
aujourd' hui , les Canadiens et les Canadiennes peuvent se enorgueillir de le succès de leur pays . 
notre économie est dynamique , notre société est forte et démocratique et nos concitoyens sont solidaires les uns de les autres . 
nous sommes reconnus dans le monde entier pour notre qualité de vie . 
nous bâtirons une meilleure qualité de vie pour le ensemble de les Canadiens et de les Canadiennes - nos enfants , nous - mêmes et nos concitoyens . 
un Canada fort et uni 
une excellente qualité de vie pour les Canadiens et les Canadiennes est indissociable de un Canada fort et uni . 
le gouvernement maintiendra une approche globale pour renforcer le unité de notre pays . 
toutes ses actions serviront à raffermir le Canada en rehaussant la qualité de vie de les Canadiens . 
notre régime fédéral nous permet de apprécier à sa juste valeur ce qui fait la force de chacune de les régions de notre pays . 