ouverture DE LA DEUXIÈME SESSION DE LA 36E LÉGISLATURE 
le Parlement , qui a été prorogé le 18 septembre 1999 , se réunit aujourd' hui à Ottawa pour le expédition de les affaires . 
la séance est ouverte à 14 h 30 , le Président étant à le fauteuil . 
le Président donne lecture de une lettre de le secrétaire de la Gouverneure générale annonçant que Leurs Excellences la Gouverneure générale et John Ralston Saul arriveront à le entrée de la Tour de la Paix à 14 h 50 le mardi 12 octobre 1999 et que , lorsque on aura avisé Leurs Excellences que tout est en place , ils se rendront à la Chambre de le Sénat pour ouvrir officiellement la deuxième session de le trente - sixième Parlement de le Canada . 
le huissière de le bâton noir apporte le message suivant : 
monsieur le Président , ce est le désir de Son Excellence la Gouverneure générale que cette honorable Chambre se rende immédiatement auprès de elle dans la salle de le honorable Sénat . 
en conséquence , le Président et les membres de les Communes se rendent à le Sénat . 
et de retour à la Chambre de les communes : 
